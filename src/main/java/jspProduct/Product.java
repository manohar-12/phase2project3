package jspProduct;

public class Product {
	private int pid;
	private String s;
	private int price;
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	public int getpid() {
		return pid;
	}
	public void setpid(int pid) {
		this.pid = pid;
	}
	public String getpname() {
		return s;
	}
	public void setpname(String s) {
		this.s = s;
	}
	
}
